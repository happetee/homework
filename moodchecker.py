mood = input("What mood are you in? ")
if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("It's ok to be sad.")
elif mood == "excited":
    print("Waauw! You're really buzzing!")
elif mood == "relaxed":
    print("Taking it easy. I dig it.")
else :
    print("I don't recognise this mood")